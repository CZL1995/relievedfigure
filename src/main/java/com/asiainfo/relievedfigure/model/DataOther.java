public class DataOther {
    private String flow;
    private String flowCost;
    private String callTime;
    private String localCall;
    private String feeCost;

    public String getFlow() {
        return flow;
    }

    public void setFlow(String flow) {
        this.flow = flow;
    }

    public String getFlowCost() {
        return flowCost;
    }

    public void setFlowCost(String flowCost) {
        this.flowCost = flowCost;
    }

    public String getCallTime() {
        return callTime;
    }

    public void setCallTime(String callTime) {
        this.callTime = callTime;
    }

    public String getLocalCall() {
        return localCall;
    }

    public void setLocalCall(String localCall) {
        this.localCall = localCall;
    }

    public String getFeeCost() {
        return feeCost;
    }

    public void setFeeCost(String feeCost) {
        this.feeCost = feeCost;
    }
}
